// Animate stats on scroll
const statsObserverOptions = {
    threshold: 0.5,
    rootMargin: '0px 0px -100px 0px'
};

const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumbers = entry.target.querySelectorAll('.stat-number');
            statNumbers.forEach(stat => {
                const target = parseInt(stat.textContent);
                animateCounter(stat, target);
            });
            statsObserver.unobserve(entry.target);
        }
    });
}, statsObserverOptions);

// Observe stats section
const statsSection = document.querySelector('.about-stats');
if (statsSection) {
    statsObserver.observe(statsSection);
}

// Counter animation function
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    const timer = setInterval(() => {
        start += increment;
        if (start >= target) {
            element.textContent = target + '+';
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(start) + '+';
        }
    }, 16);
}

// Enhanced Reservation Form Handling
const reservationForm = document.getElementById('reservation-form');

if (reservationForm) {
    reservationForm.addEventListener('submit', function(e) {
        e.preventDefault();
        
        // Get form data
        const formData = new FormData(this);
        const data = Object.fromEntries(formData);
        
        // Enhanced validation
        const errors = [];
        
        if (!data.name || data.name.trim().length < 2) {
            errors.push('Please enter a valid name (minimum 2 characters)');
        }
        
        if (!data.email || !isValidEmail(data.email)) {
            errors.push('Please enter a valid email address');
        }
        
        if (!data.phone || !isValidPhone(data.phone)) {
            errors.push('Please enter a valid phone number');
        }
        
        if (!data.date || !isValidDate(data.date)) {
            errors.push('Please select a valid date');
        }
        
        if (!data.time) {
            errors.push('Please select a time');
        }
        
        if (!data.guests || data.guests < 1) {
            errors.push('Please select number of guests');
        }
        
        if (errors.length > 0) {
            showNotification(errors.join('<br>'), 'error');
            return;
        }
        
        // Simulate form submission
        showNotification('Processing your reservation...', 'info');
        
        setTimeout(() => {
            showNotification('Reservation submitted successfully! We will contact you soon to confirm.', 'success');
            this.reset();
        }, 2000);
    });
}

